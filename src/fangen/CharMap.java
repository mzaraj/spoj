package fangen;

public class CharMap {

    private char star ='*';
    private char dot ='.';


    public char getStar() {
        return star;
    }

    public void setStar(char star) {
        this.star = star;
    }

    public char getDot() {
        return dot;
    }

    public void setDot(char dot) {
        this.dot = dot;
    }
}
